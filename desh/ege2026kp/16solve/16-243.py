N = 250000
m = 82264
B = 1000000

f = [0]*(N+7)
for n in range(N+6, m-3, -3):
  if n > N: f[n] = n**n
  else: f[n] = 3*n + f[n + 6] + f[n+3]
  f[n] = f[n] % B

print( f[m] )

